    die("Connection failed: " . mysqli_connect_error());
}

// Insert Movie Information
if (isset($_POST['btn'])) {
    $mname = $_POST['mname'];
    $image = $_FILES['img']['name'];
    $temp_image = $_FILES['img']['tmp_name'];
    $target_dir = "img/";
    $target_file = $target_dir . basename($image);
    move_uploaded_file($temp_image, $target_file);

    $trailer = $_FILES['trailer']['name'];
    $temp_trailer = $_FILES['trailer']['tmp_name'];
    $target_dir_trailer = "trailers/";
    $target_file_trailer = $target_dir_trailer . basename($trailer);
    move_uploaded_file($temp_trailer, $target_file_trailer);

    $rating = $_POST['rating'];
    $duration = $_POST['duration'];
    $category = $_POST['category'];
    $date = $_POST['date'];
    $about = $_POST['about'];

    $sql = "INSERT INTO `about_movie` (`mname`, `img`, `trailer`, `rating`, `hour`, `category`, `releasing_date`, `about_movie`)
            VALUES ('$mname', '$target_file', '$target_file_trailer', '$rating', '$duration', '$category', '$date', '$about')";

    if (mysqli_query($conn, $sql)) {
        echo "Data Entered Successfully";
    } else {
        echo "Data Not Entered. Error: " . mysqli_error($conn);
    }
}

// Retrieve and Display Movie Information
$sql = "SELECT * FROM `about_movie`";
$result = mysqli_query($conn, $sql);

mysqli_close($conn);
